public class Secciones {
    private int id;
    private String seccion;

    public Secciones(int id, String seccion) {
        this.id = id;
        this.seccion = seccion;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSeccion() {
        return seccion;
    }

    public void setSeccion(String seccion) {
        this.seccion = seccion;
    }
    
    

}
